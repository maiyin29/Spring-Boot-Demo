package com.duyi.bean;




public class Guest {
    private String name;

    private String role;

    public Guest() {
    }

    public Guest(String name, String role) {
        this.name = name;
        this.role = role;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
